public class FirstP {
	public static void main(String[] args) {
		
		for(int i=1;i<10;i++)
		{
			System.out.println("I am Batman");
		}
		
		System.out.println("--------------------------------------------------------------------");
		
		for(int i=1;i<10;i++)
		{
			System.out.println("I am Batman  "+ i);
		}
		
		System.out.println("--------------------------------------------------------------------");
       
		int p=10;
        while(p>=1)
        {
	       System.out.println(p);
          	p--;
        }
             
        
        System.out.println("--------------------------------------------------------------------");

        int k=10;
        do 
        {
	        System.out.println(k);
	        k--;
        }while(k>=1);
         
        System.out.println("--------------------------------------------------------------------");

        int i=10;
        while(i>=1)
        {
	      System.out.println("Hello World");
	        i--;
         }

         System.out.println("--------------------------------------------------------------------");

        int l=1;
        do 
        {
           System.out.println("Hello World");
	       l++;
        }while(l<11);
        
        int P=0;
        while(P<=10) {
        	System.out.println(P);
        }
      
	}
}
